/// Parse comma-separated GPU indices from string
std::set<uint32_t> parseGpuIndices(const std::string& str)
{
	std::set<uint32_t> indices;
	if(str.empty()) return indices;

	std::istringstream ss(str);
	uint32_t idx;
	while(ss >> idx)
	{
		indices.insert(idx);
		while(ss.peek() == ',' || ss.peek() == ' ')
			ss.ignore();
	}
	return indices;
}

TuneMode parseTuneMode(const std::string& str)
{
	if(str == "quick") return TuneMode::Quick;
	if(str == "exhaustive") return TuneMode::Exhaustive;
	return TuneMode::Balanced;
}

TuneTarget parseTuneTarget(const std::string& str)
{
	if(str == "efficiency") return TuneTarget::Efficiency;
	if(str == "balanced") return TuneTarget::Balanced;
	return TuneTarget::Hashrate;
}

/// Write the winning AMD candidate to the amd.txt config
bool writeAmdConfig(uint32_t device_index, const AmdCandidate& winner, const std::string& config_path)
{
	FILE* f = fopen(config_path.c_str(), "w");
	if(!f) return false;

	fprintf(f, "// Generated by n0s-ryo-miner --autotune\n");
	fprintf(f, "// Device %u: intensity=%zu worksize=%zu\n\n", device_index, winner.intensity, winner.worksize);
	fprintf(f, "\"gpu_threads_conf\" : [\n");
	fprintf(f, "  { \"index\" : %u,\n", device_index);
	fprintf(f, "    \"intensity\" : %zu, \"worksize\" : %zu,\n", winner.intensity, winner.worksize);
	fprintf(f, "    \"affine_to_cpu\" : false, \"strided_index\" : 0, \"mem_chunk\" : 2,\n");
	fprintf(f, "    \"unroll\" : 1, \"comp_mode\" : true, \"interleave\" : 40\n");
	fprintf(f, "  },\n");
	fprintf(f, "],\n\n");
	fprintf(f, "\"auto_tune\" : 0,\n");
	fprintf(f, "\"platform_index\" : 0,\n");
	fclose(f);

	printer::inst()->print_msg(L0, "AUTOTUNE: AMD config written to %s", config_path.c_str());
	return true;
}

/// Write the winning NVIDIA candidate to the nvidia.txt config
bool writeNvidiaConfig(uint32_t device_index, const NvidiaCandidate& winner, const std::string& config_path)
{
	FILE* f = fopen(config_path.c_str(), "w");
	if(!f) return false;

	fprintf(f, "// Generated by n0s-ryo-miner --autotune\n");
	fprintf(f, "// Device %u: threads=%u blocks=%u bfactor=%u\n\n",
		device_index, winner.threads, winner.blocks, winner.bfactor);
	fprintf(f, "\"gpu_threads_conf\" :\n[\n");
	fprintf(f, "  { \"index\" : %u,\n", device_index);
	fprintf(f, "    \"threads\" : %u, \"blocks\" : %u,\n", winner.threads, winner.blocks);
	fprintf(f, "    \"bfactor\" : %u, \"bsleep\" : 0,\n", winner.bfactor);
	fprintf(f, "    \"affine_to_cpu\" : false, \"sync_mode\" : 3,\n");
	fprintf(f, "    \"mem_mode\" : 1,\n");
	fprintf(f, "  },\n");
	fprintf(f, "],\n");
	fclose(f);

	printer::inst()->print_msg(L0, "AUTOTUNE: NVIDIA config written to %s", config_path.c_str());
	return true;
}

